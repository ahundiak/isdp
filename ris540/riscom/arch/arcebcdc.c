extern unsigned char ARC_ch_asc_to_ebc(
	unsigned char c)
{
	return(a2e_table[c]);
}

extern char ARC_ch_ebc_to_asc(
	unsigned char c)
{
	return(e2a_table[c]);
}


extern void ARC_data_asc_to_ebc(
	unsigned char *s,
	int len)
{
	int i;

	ARC_DBG((" ARC_data_asc_to_ebc: %08x, %d\n", s, len));
	ARC_DBG((" <%.*s>\n", len, s));
	for (i = 0; i < len; i++)
		s[i] = ARC_ch_asc_to_ebc((unsigned char)(s[i]));
}


extern void ARC_data_ebc_to_asc(
	unsigned char *s,
	int len)
{
	int i;

	ARC_DBG((" ARC_data_ebc_to_asc: %08x, %d\n", s, len));
	for (i = 0; i < len; i++)
		s[i] = ARC_ch_ebc_to_asc((unsigned char)(s[i]));
	ARC_DBG((" <%.*s>\n", len, s));
}


extern void ARC_str_asc_to_ebc(
	unsigned char *s)
{
	unsigned char *ptr;

	ARC_DBG((" ARC_str_asc_to_ebc: %08x\n", s));
	if (s)
	{
		ARC_DBG((" <%s>\n", s));
		ptr = s;
		while(*ptr)
		{
			*ptr = ARC_ch_asc_to_ebc((unsigned char)(*ptr));
			ptr++;
		}
	}
}

extern void ARC_str_ebc_to_asc(
	unsigned char *s)
{
	unsigned char *ptr;

	ARC_DBG((" ARC_str_ebc_to_asc: %08x\n", s));
	if (s)
	{
		ptr = s;
		while(*ptr)
		{
			*ptr = ARC_ch_ebc_to_asc((unsigned char) *ptr);
			ptr++;
		}
		ARC_DBG((" <%s>\n", s));
	}
}


	/*
	** We need to do a strcmp of ASCII characters, but we must 
	** use the EBCDIC collating sequence.  So, instead of comparing
	** ch == ch, we test for a2e[ch] == a2e[ch].
	**
	**	strcmp("v1","vv1")
	**	ARC_ebcdic_strcmp("v1","vv1")
	**
	** should give the same result with a different sign.
	*/

extern int ARC_ebcdic_strcmp(
	unsigned char *s1, 
	unsigned char *s2)
{
	int status;
	unsigned int ch1,ch2;

	for (   ; 1 ; ++s1, ++s2)
	{
		/*
		** ch1, ch2 MUST avoid sign extension
		*/
		ch1 = (unsigned) a2e_table[(unsigned char)(*s1)];
		ch2 = (unsigned) a2e_table[(unsigned char)(*s2)];

		/*
		** Now that the are safely in 4-byte entities, they can
		** be treated as integers (no sign extension) to get a signed 
		** difference.
		*/
			
		status = ((int)ch1) - (int)ch2;
		if (status || (!ch1) || (!ch2))
			return status;
	}
}
