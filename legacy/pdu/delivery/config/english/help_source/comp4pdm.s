<TP>Create_cat1
:Catalog Operations Form:
The Catalog Operations<T> form appears when you select the
Catalog<T> button on the PDM/PDU<T> form.  This form has two
modes of operation:

1.   New<T> --- This mode lets you add a new catalog.

2.   Change<T> --- This mode lets you change or modify existing
     catalogs.

<S>New Mode<T>

The New<T> mode of the Catalog Operations<T> form is used to add
new catalogs.  The new catalog can be created from scratch,
or copied from an existing catalog.  These catalogs will be
used to store information about parts, drawings, assemblies.
When adding new catalogs, I/PDM gives you the ability to
group similar catalogs based on similarities and then
separate them based on their differences.  This is known as
classifications<T>. Because catalog classifications group similar
catalogs, they can be very helpful when searching for a
particular part from the catalogs.  I/PDM does not require you
to classify catalogs, but it can be a very powerful tool in
effectively managing a large amount of data.  If you choose to
use classifications, you should define the proper
classifications before creating catalogs.

The form contains different fields based on the mode in which
you are working.

<S>Form Fields ---<T>

<S>New Button<T>

This button invokes the New<T> mode of operation for the
Catalog Operations<T> form.

<S>Catalog Name Field<T>

This field contains the name for the catalog being added.  If a
catalog is keyed in, it is validated for length and valid
characters.

<S>Description Field<T>

This field contains the description for the catalog being
added.

<S>ACL Field<T>

This field contains the ACL for the catalog being added.  This
information can be keyed in.  For more information on ACLs,
refer to Access Control Lists<T> in this manual.

<S>Catalog Classification Palette<T>

This palette is used to add classifications for grouping
catalogs. It is very important that catalogs be properly
classified when created. If a classification does not exist for a
catalog, you should create one before adding the new
catalog.

The following steps describe how to add a classification.

1.   Enter a data point inside the Catalog Classification<T>
     palette.

     A small subform displays an entry,<T> new category<T>, followed
     by a list of available classifications.  If no classifications
     exists,<T> new category<T> is the only entry listed on the
     subform.<T>

2.   If no classifications exist or the desired classification
     does not exist:

     a.   Select the new category<T> entry from the list.

          The cursor appears in the<T> Catalog Classification<T> palette.<T>

     b.   Key in the name of the classification and press
          <Return>.

     c.   Add any lower level classifications that might be
          needed.

     d.   After you have added the desired classifications, enter
          the information for the catalog to be added.

     If the desired classification already exists:

     a.   Highlight the desired classification name from the list.

          The list disappears and the classification name appears
          in the<T> Catalog Classification<T> palette.<T>

     b.   Proceed to enter the information for the catalog to be
          added.

<S>Default Usage ID Field<T>

This field appears when you are creating a parametric
catalog.  The field lets you enter a usage id for all parts in a
parametric catalog placed in an assembly.

<S>Template Catalog Field<T>

This field is used is create a new catalog based on the
attributes of an existing catalog.  You can enter the name of
the existing catalog that you wish to copy in this field.  When
you select the List<T> button next to the field, a list of existing
catalogs appears.  When you highlight the catalog in the list
to be copied, the list disappears and the catalog name
appears in this field.

<S>Catalog Type Toggle<T>

This toggle indicates the type of catalog to be created.  The
possible values for this toggle are Standard<T> and Parametric<T>.

<S>Change Mode<T>

The Change<T> mode of the Catalog Operations<T> is used to make
changes to existing catalogs.  When catalogs are changed,
they can be assigned to a new classification.  This form is
also used to delete existing catalogs.

<S>Form Fields ---<T>

<S>Change Button<T>

This button invokes the Change<T> mode of operation for the
Catalog Operations<T> form.

<S>Catalog Name Field<T>

This field contains the name for the catalog being changed.
You can enter the catalog name by key-in or select it from a
list generated by the List<T> button next to the Catalog Name<T>
field.

<S>Description Field<T>

This field contains the description for the catalog being
changed.

<S>Catalog Classification Palette<T>

In Change<T> mode, this palette displays the classification to
which the catalog being changed belongs.

<S>Catalog Type Toggle<T>

This toggle is not active in Change<T> mode.

<S>Delete Catalog Button<T>

This button becomes active in Change<T> mode and lets you
