<TP>bom_contents
:BOM Contents/Mapping Form:
The BOM Contents/Mapping<T> form appears when you select
the BOM Mapping<T> button on the Reports<T> form and lets you
map user-defined attributes to BOM attributes or change to
existing attributes.  The system automatically maps BOM
attributes to catalogs with the same name.  This form lets you
map those attributes that are not automatically mapped and
to add new BOM attributes.

<S>Form Fields ---<T>

<S>BOM Attribute Field<T>

This field displays the attribute names for the BOM.  The
maximum attribute size is 40 characters (A-Z, 0-9, or
underbar) and must start with a lowercase letter.  The
attribute name cannot have an underbar(_) as the second
character.

<S>Data Type Field<T>

This field displays the data type for the BOM attribute.

<S>Synonym Field<T>

This field displays the synonym for the BOM attribute.  The
synonym is the name that appears on the BOM.  The synonym
may contain spaces and uppercase letters.  The maximum
size for a synonym is 40 characters, although only 26
characters are displayed in the form.

<S>Mapping Palette<T>

This palette displays the catalog attribute that is be mapped
to the BOM attribute.  You can map the BOM attributes by
entering the information in the fields of this palette and
selecting the Map Attribute<T> button.

<S>BOM Attribute Field<T>

This field displays the BOM attribute to be mapped.  You can
use the List<T> button next to this field to display a complete list
of BOM attributes.

<S>Catalog Name Field<T>

This field displays the current catalog.  You can use the List<T>
button beside this field to view a complete list of catalogs.  If
you are using catalog classifications, the List<T> button displays
a list of valid classifications.  You can select the appropriate
classification to display a list of catalogs within the
classification.

<S>Catalog Attribute Field<T>

This field displays the catalog attribute to be mapped.  You
can use the List<T> button next to this field to display a complete
list of attributes contained in the catalog displayed in the
Catalog<T> field.

<S>Map Attribute Button<T>

This button maps the BOM attribute to the corresponding
catalog attribute.  The information in the Mapping<T> palette
must be complete before the Map Attribute<T> button is selected.

<S>Attribute Information Palette<T>

This palette displays information about the BOM attribute to
be added. You can add BOM attributes by entering information
in the fields of this palette and selecting the Add Attribute<T>
button.

<S>BOM Attribute Field<T>

This field displays the BOM attribute being added.

<S>Data Type Field<T>

This field displays the data type of the BOM attribute being
mapped.

<S>Synonym Field<T>

This field displays the synonym of the BOM attribute being
mapped.

<S>Add Attribute Button<T>

This button adds attributes to the BOM.  You must enter
information in the fields of this palette before selecting the
Add Attribute<T> button.

<ETP>
