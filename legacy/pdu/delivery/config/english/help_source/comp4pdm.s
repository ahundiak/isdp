<TP>create_cat2
:Catalog Attributes Form:
The Catalog Attributes<T> form appears when you select the Exit
and Save<T> button on the Catalog Operations<T> form and lets you
change the synonyms of the required attributes, add user
defined attributes and assign protections.  The choices
expressed in this form are actually used when parts are added
to the catalog.

<S>Form Fields ---<T>

<S>Static Button<T>

This button identifies the static attributes that will be added
to the catalog.  These attributes are added as columns to the
catalog table in the database.  Values for static attributes are
defined when adding parts.

<S>Dynamic Button<T>

This button identifies the dynamic attribute that will be added
to the catalog.  Dynamic attributes receive values when the
part is placed in an assembly.

<S>Dynamic Table Field<T>

This field appears when you select the Dynamic<T> button at the
top of the form to add dynamic attributes.  The table defaults
to y_<catalog><T> where <catalog> represents the catalog
being created.  You can use the default or key in another
value.

Dynamic attribute tables can be shared among several
catalogs.  The following steps describe how this is done.

1.   Create a catalog with the necessary attributes.

2.   Create another catalog that requires the same dynamic
     attributes and place the dynamic attribute table name in
     the Dynamic Attribute Table<T> field.

3.   Once you have entered the table name in the field, select
     the Exit and Save<T> button on the form.  The attribute does
     not have to be added again.

<S>Palette for Attribute Listing<T>

This palette lists the static or dynamic attributes that will be
added to the catalog.  This list will be updated when adding a
part to this catalog.  You can change the order of these
attributes by entering a data point in the Order<T> field, deleting
the value, and keying in the new value. The rows will then be
resequenced.

<S>Attribute Definition Palette<T>

This palette contains the options available for defining
catalog attributes.

<S>Range Button<T>

This toggle gives the option to define either a range of values
or a list of possible values for the attribute selected from the
Listing<T> area or an attribute you are adding to the list.  These
values must be entered prior to adding or changing the
attribute.  If an attribute is selected in the Listing<T> field, the
values listed are associated with the selected attribute. After
you select this button, enter the minimum and maximum
range values in the Minimum Value<T> and Maximum Value<T> fields.

<S>List Button<T>

This button displays a list of possible values for the attribute.
You can also use the list to add possible values for the
attribute.

<S>Function Button<T>

This button lets you assign a function to the attribute value.
When you select the Function<T> button, a Function<T> field is
displayed in which you key in the PPL function name.  I/PDM
looks locally for the function and makes sure that it is unique.
If the function is not local, I/PDU looks for it in the function
storage area.

<S>Minimum Value<T>

The field where you enter the minimum value for the range of
values for an attribute.

<S>Maximum Value<T>

The field where you enter the maximum value for range of
values for an attribute.

<S>Listing Area<T>

The field displays when you select the List<T> or Function<T> button
to display a list of possible values for the attribute or
function.

<S>Protections Palette<T>

These toggles let you set protections for the attributes.

<S>Read Toggle<T>

This toggle lets you give the user read access to this attribute.
For new attributes, it is defaulted to Yes.

<S>Write Toggle<T>

This toggle lets you give the user write access to this
attribute.  For new attributes, the toggle is set to Yes. The Yes
value lets you assign values to this attribute when adding a
part to the catalog.

<S>Update Toggle<T>

This toggle lets you give the user update access to this
attribute.  For example, when performing Change Part<T> you
can update information on an existing part in this catalog.
For new attributes, the toggle is set to Yes.

<S>Input Required Toggle<T>

This toggle lets you identify whether users are required to
enter values for the attributes when adding a part to the
catalog or modifying part information.  For new attributes,
the toggle is set to No.

<S>Attribute Information Palette<T>

This palette displays information about attributes to be added
or changed.

<S>Attribute Name Field<T>

This field lets you key in an attribute name to be added.  When
you key in an attribute name, the name is validated.  If the
attribute name is not valid, an error message is returned and
the field is cleared. This field is also used to change an
attribute name.

<S>Datatype Field<T>

This field lets you key in the datatype for the attribute to be
added.  This field is also used to change an attribute datatype,
(increasing or decreasing the size of a character datatype).
For more information on data types, refer to the RIS Reference
Manual DNA0032<T>.

<S>Synonym Field<T>

This field lets you key in the synonym for the attribute to be
added. This field is also used to change an attribute synonym.

<S>Default Value Field<T>

This field lets you key in the default value for the attribute to
be added.  This field is also used to change the default value
for an attribute.  This value will be displayed at add part time.

<S>Attribute Commands Palette<T>

This palette contains commands that let you perform
operations on catalog attributes.

<S>Show System Attributes Toggle<T>

This toggle indicates if the system attributes will be displayed
when this form appears.

<S>Add Attribute Button<T>

This button lets you add the attribute keyed in the Attribute
Name<T> field.  You must have all toggles set and range or list
values set before you add the attribute.  The command lets
you key in the order, synonym, and default value for the
attribute. After you add the attribute, its name appears in the
attribute name column in the attribute multi-column list.

<S>Change Attribute Button<T>

This button lets you change the selected attributes.  You can
change synonym or size for the datatype on required
attributes.

<S>Note:<T>

When this form is checked off, the Catalog Operations<T> form
reappears. <F> „<T>

