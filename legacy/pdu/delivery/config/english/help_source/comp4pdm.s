<TP>forms
:Using Forms:
Many commands use forms that gather information used by
the command.  A form contains items called gadgets.  A
gadget is a portion of a form that gathers and displays
information from and to the user of the command.

<S>Form Descriptions<T>

The following sections briefly describe the forms used in
I/PDM.  The forms are illustrated and described in greater
detail later in the manual.  Many of the forms contain the
Control Bar buttons that have already been described.  Those
buttons will not be described in the form descriptions.

<XR>login  Login to PDM<T> --- This form logs into I/PDM server and
environment.

<XR>list  List<T> --- This form displays information you request when you
select a list button.  There are two types of List<T> forms: Single
Column Field<T> and Multi-column Field<T>.

<XR>local_files  PDM/PDU<T> --- This form is the main menu for I/PDM-PDU
commands.

<XR>seed_form  New File Creation<T> --- This form appears when you select the
Retrieve for Design<T> button on the PDM/PDU<T> form to retrieve a
part that has never been checked in.  The form lets you add a
reference seed file to a part.

<XR>Create_cat1  Catalog Operations<T> --- This form lets you perform different
catalog operations on different catalogs displayed on the list
on the form.

<XR>create_cat2  Catalog Attribute<T> --- This form appears when you select the
Require Attributes Display<T> or User Define Attributes<T> options
from the Catalog Name<T> form.  The forms lets you change the
synonyms of the required attributes, add user defined
attributes, and assign protections, defaults, ranges for
validation, and list of values.

<XR>part_form  Part Operation<T> --- This form lets you perform specific part
operations.  This form appears in four modes: New, Similar,
Change,<T> and Review<T>.

<XR>part_listing  Part Listing<T> --- This forms lists parts found during a search
when performing assembly design.

<XR>parmoper  Parametric Parts Operations<T> --- This form lets you perform
operations on parametric parts.

<XR>MOAddPart  Parametric Attributes<T> --- This form lets you assign values to
attributes for  parametric parts.

<XR>def_lib  Define Support Library<T> --- This form lets you define support
libraries to use for parametric part operations.

<XR>MOPartSelec2  Part Selection<T> --- This form lets you perform part operations
such as Place Part<T> and Replace Part<T>, as well as search for
parts in the database.

<XR>search_options  Name<T> --- This form lets you load previously saved search
criteria or save new search criteria.

<XR>sort  Sort Parts<T> --- This form lets you sort the results from a
search before the catalog or part listing is displayed.

<XR>sort_options  Load Sort Conditions<T> --- This form lets you load previously
saved sort conditions.

<XR>ssort  Save Sort Conditions<T> --- This form lets you save new sort
conditions.

<XR>where_used  Part Relationship Criteria<T> --- This form lets you display
where a selected part is used in an assembly or what parts
are contained in the selected part.

<XR>cancel_list  Cancel Part Check Out<T> --- This form lets you cancel the
checkout of a part.

<XR>design_form  Design Operations<T> --- This form accesses to all commands
relative to building an assembly.

<XR>placeOpt  Placement Option<T> --- This form appears when you select the
Placement Options<T> button on the Part Selection<T> form and lets
you determine how a part is placed.

<XR>topdown  Identify Part<T> --- This form is displayed during a topdown
assembly design and lets you identify an existing part to
associate with a collection of graphic elements.

<XR>Dynamic  Dynamic Attribute Information<T> --- This form lets you place
dynamic attributes when executing the Place Part<T> and
Replace Part<T> commands.

<XR>define_assy  Define/Modify Assembly Structure<T> --- This form lets you
define and modify the structure of an assembly.

<XR>report_form  Reports<T> --- This form lets you define and generate reports.

<XR>reportgen  Define Report Format<T> --- This form lets you define the
format for reports.

<XR>bom_contents  BOM Contents/Mapping<T> --- This form lets you define and
modify the BOM contents and BOM mapping.

