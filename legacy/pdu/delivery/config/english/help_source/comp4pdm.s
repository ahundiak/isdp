The Part Selection<T> form lets you perform part operations such
as Place Part<T> and Replace Part<T>, as well as perform searches
for parts, in the database.  You can search for a part in a single
catalog or in a number of catalogs in the database.  The form
is invoked in several ways.  It appears when you select the
Search for Parts<T> button from the PDM/PDU<T> form, the Place
Part<T> button or Replace Part<T> button from the Design Operations<T>
form, or the Search<T> button that appears on several forms.

<S>Form Fields ---<T>

<S>Project Field<T>

This field lets you limit the search for selected parts to those
that belong to the indicated project.  Selecting this field
displays a list of all projects.  You can then select the project
from this list.

<S>Catalog Field<T>

This field contains the name of the catalog when you are
limiting the search to a single catalog.

<S>Description Field<T>

This field contains the description of the catalog displayed in
the Catalog<T> field.

<S>Classification Buttons<T>

This button performs several functions.  Selecting this button
invokes a list that displays the entries, Common Attributes,
Display Catalogs<T>, and a list of classifications and catalogs at
this level.

Highlighting Common Attributes<T> displays a list of common
attributes at this classification level.

Highlighting the Display Catalogs<T> displays a list of all
catalogs in the database.

Highlighting a classification invokes another list that displays
the entries, Common Attributes, Display Catalogs<T>, and a list of
classifications and catalogs at this level. You can continue to
select the classification until you get to the level containing
the desired catalog.

<S>Static Attr/Dynamic Attr Toggle<T>

This toggle indicates the attribute type on which the search
will be conducted.  If the toggle is set to Static Attr<T> the
system searches on the static attributes defined on the
Catalog Attributes<T> form.  If the toggle is set to Dynamic Attr<T>
the system searches on the dynamic attributes defined on the
Catalog Attributes<T> form.

<S>Replace All/Replace One Toggle<T>

This toggle appears on the form only when the Replace Part<T>
on the Design Operations<T> form is selected and lets you
replace all occurrences or a single occurrence of the selected
part.

<S>Show Graphics Button<T>

This button displays the graphic file for the selected part.

<S>Search Button<T>

This button executes a search based on the search criteria
displayed in the attribute listing palette at the bottom on the
form.  You can conduct searches based on new criteria or on
previously defined criteria.

<S>Placement Options Button<T>

This button displays the Placement Options<T> form.

<S>Expand Listing Button<T>

This button invokes the Expanded Listing<T> form, which displays
a detailed list of the values returned from executing a search.

<S>Selected Value Palette<T>

Use the Minus<T> and Plus<T> buttons to scroll through the parts to
locate a specific part.  The field between these buttons tells
the number of parts found in the search.

<S>Selection Criteria Palette<T>

This palette contains buttons used in search and sort
operations. Before selecting any of these buttons, you must
select the Convert to Permanent Window<T> button at the top of
this form.

<S>Load Button<T>

This button lets you load previously defined search criteria.
When you select this button a subform appears that lets you
enter the name of the previously defined search criteria.

<S>Save Button<T>

This button lets you save search criteria.  When you select this
button a subform appears that lets you enter the name to save
the new search criteria.

<S>Sort Button<T>

This button invokes the Sort Parts<T> form that lets you define
sort conditions for the results of the search.

<S>Part Relation Button<T>

This button displays the Part Relationship Criteria<T> form.

