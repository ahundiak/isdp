<TP>PDEnSv
:Exit Design Without Save:
This command lets you exit a design session without saving
any changes made to the design file during the design
session.

<S>Before Using This Command<T>

A part must be checked out and retrieved for design.

<S>Operating Sequence<T>

1.   Select the Exit Without Save<T> button on the Design
     Operations<T> form.

     Exit design without save (y/n)? [y]<T>

2.   Press <Return> to exit the design session without saving
     any changes.

     The part remains checked out.

                                              OR

     Key in n<T> and press <Return> to remain in the
           design session.


<ETP>
