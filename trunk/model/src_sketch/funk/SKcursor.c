static int reversed = 0;
#endif

void SKset_cursor(int index)
{
  int hotspot[2];
#ifdef BIG_ENDIAN
  int  i, ii;
  char *p_byte, t_byte;
  long *c_d, *c_e;
#endif
  extern int LCset_window();

  if (index < 0 || index > 22) return;
  hotspot[0]=cursors[index].hs_x;
  hotspot[1]=cursors[index].hs_y;

#ifdef BIG_ENDIAN

    /*
     *  reverse the byte order in the data and enable patterns the first time
     */
    if ( !reversed )
    {
       for ( ii = 0; ii < 23; ii++ )
       {
          c_e = cursors[ii].enable;
          c_d = cursors[ii].data;
          for (i = 0; i < 32; i++)
          {
              p_byte = (char *)&c_d[i];
              t_byte = p_byte[3];
              p_byte[3] = p_byte[0];
              p_byte[0] = t_byte;
              t_byte = p_byte[2];
              p_byte[2] = p_byte[1];
              p_byte[1] = t_byte;
      
              p_byte = (char *)&c_e[i];
              t_byte = p_byte[3];
              p_byte[3] = p_byte[0];
              p_byte[0] = t_byte;
              t_byte = p_byte[2];
              p_byte[2] = p_byte[1];
              p_byte[1] = t_byte;
          }
       }
       reversed = 1;
    }

#endif
  LCset_window (1, &cursors[index].enable, &cursors[index].data, &hotspot);
}      

void SKreset_cursor()
{
  extern int LCset_window();
  LCset_window (0, 0, 0, 0);
}

static short animate=0;

void SKclock_cursor()
{
  animate = animate % 8;
  SKset_cursor (15 + animate++);
}

